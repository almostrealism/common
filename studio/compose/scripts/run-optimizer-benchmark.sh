#!/bin/bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
COMPOSE_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
WORKING_DIR="/Users/Shared/Music"

cd "$WORKING_DIR"
mkdir -p results/logs

CLASSPATH_CACHE="$COMPOSE_DIR/.classpath-cache"

if [ ! -f "$CLASSPATH_CACHE" ] || [ "$COMPOSE_DIR/pom.xml" -nt "$CLASSPATH_CACHE" ]; then
    echo "Resolving classpath..."
    mvn -f "$COMPOSE_DIR/pom.xml" dependency:build-classpath \
        -Dmdep.outputFile="$CLASSPATH_CACHE" -q
fi

CP="$COMPOSE_DIR/target/classes:$(cat "$CLASSPATH_CACHE")"

: "${AR_BENCHMARK_DURATIONS:=8,16,32,64}"
: "${AR_BENCHMARK_CHANNEL:=0}"
: "${AR_BENCHMARK_MULTI_CHANNELS:=all}"
: "${AR_BENCHMARK_RUN_MULTI:=true}"
: "${AR_BENCHMARK_RUN_SINGLE:=true}"
: "${AR_BENCHMARK_FEATURE_LEVEL:=7}"
: "${AR_BENCHMARK_STEMS:=true}"
: "${AR_BENCHMARK_REPEATS:=1}"
: "${AR_BENCHMARK_REPORT:=results/audio-scene-benchmark.csv}"
: "${AR_BENCHMARK_PROFILE_DIR:=results/profiles}"
: "${AR_BENCHMARK_TIMELINE_DIR:=results/timelines}"
: "${AR_BENCHMARK_DISABLE_PROFILE:=false}"
: "${AR_BENCHMARK_INTER_REPEAT_PAUSE_MS:=0}"
: "${AR_BENCHMARK_CLEAR_AUDIO_CACHE:=false}"
: "${AR_BENCHMARK_REUSE_SCENE:=false}"
: "${AR_BENCHMARK_HEAP:=8g}"

mkdir -p "$AR_BENCHMARK_PROFILE_DIR" "$AR_BENCHMARK_TIMELINE_DIR"

exec java -cp "$CP" "-Xmx${AR_BENCHMARK_HEAP}" \
    -DAR_HARDWARE_MEMORY_SCALE=6 \
    -DAR_PROFILE_MULTIPLE_SOURCES=enabled \
    -DAR_HARDWARE_METADATA=disabled \
    -DAR_INSTRUCTION_SET_MONITORING=failed \
    -DAR_PATTERN_WARNINGS=disabled \
    -DAR_RINGS_LIBRARY="/Users/Shared/Music/Samples" \
    -DAR_BENCHMARK_DURATIONS="$AR_BENCHMARK_DURATIONS" \
    -DAR_BENCHMARK_CHANNEL="$AR_BENCHMARK_CHANNEL" \
    -DAR_BENCHMARK_MULTI_CHANNELS="$AR_BENCHMARK_MULTI_CHANNELS" \
    -DAR_BENCHMARK_RUN_MULTI="$AR_BENCHMARK_RUN_MULTI" \
    -DAR_BENCHMARK_RUN_SINGLE="$AR_BENCHMARK_RUN_SINGLE" \
    -DAR_BENCHMARK_FEATURE_LEVEL="$AR_BENCHMARK_FEATURE_LEVEL" \
    -DAR_BENCHMARK_STEMS="$AR_BENCHMARK_STEMS" \
    -DAR_BENCHMARK_REPEATS="$AR_BENCHMARK_REPEATS" \
    -DAR_BENCHMARK_REPORT="$AR_BENCHMARK_REPORT" \
    -DAR_BENCHMARK_PROFILE_DIR="$AR_BENCHMARK_PROFILE_DIR" \
    -DAR_BENCHMARK_TIMELINE_DIR="$AR_BENCHMARK_TIMELINE_DIR" \
    -DAR_BENCHMARK_DISABLE_PROFILE="$AR_BENCHMARK_DISABLE_PROFILE" \
    -DAR_BENCHMARK_INTER_REPEAT_PAUSE_MS="$AR_BENCHMARK_INTER_REPEAT_PAUSE_MS" \
    -DAR_BENCHMARK_CLEAR_AUDIO_CACHE="$AR_BENCHMARK_CLEAR_AUDIO_CACHE" \
    -DAR_BENCHMARK_REUSE_SCENE="$AR_BENCHMARK_REUSE_SCENE" \
    org.almostrealism.studio.optimize.AudioSceneBenchmark "$@"
